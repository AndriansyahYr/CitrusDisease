    private void btnTrainingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTrainingActionPerformed
        // TODO add your handling code here:
        akurasi1.setText("");
        BufferedImage citra = null;
        final String path_training1 = "Training/Training 1";
        final String path_preprocessing = "Preprocessing";
        final String path_otsu = "Otsu";
        final DefaultTableModel dataSetModel = (DefaultTableModel) tabelLatih.getModel();
        while(dataSetModel.getRowCount() > 0){
            dataSetModel.removeRow(0);
        }
        final DefaultTableModel dataSetMode2 = (DefaultTableModel) tabelKlas1.getModel();
        while(dataSetMode2.getRowCount() > 0){
            dataSetMode2.removeRow(0);
        }
        Thread t = new Thread(){
            @Override
            public void run(){
                final File folder = new File(path_training1);
                for(final File fileEntry : folder.listFiles()){
                        String sf = comboScale3.getSelectedItem().toString();
                        float scaleFactor = Float.valueOf(sf);
                    try{
                        BufferedImage citra = ImageIO.read(new File(path_training1+"/"+fileEntry.getName()));

                        //citra diresize 275x150
                        if(citra.getWidth()!=275&&citra.getHeight()!=150){
                            citra=img.resize(citra, 275, 150);
                        }
                        //citra asli
                        ImageIcon icon1 = new ImageIcon(citra);
                        trainAsli.setIcon(icon1);

                        //citra dicerahkan
                        RescaleOp rescleop = new RescaleOp((float)scaleFactor, 0, null);
                        citra = rescleop.filter(citra, null);
                        ImageIcon icon2 = new ImageIcon(citra);
                        trainCerah.setIcon(icon2);
                        ImageIO.write(citra, "jpg", new File(path_preprocessing+"/"+"cerah "+fileEntry.getName()));

                        //citra grayscaling
                        citra=otsu.setRGBtoGrayLevel(citra);
                        ImageIcon icon3 = new ImageIcon(citra);
                        trainGray.setIcon(icon3);
                        ImageIO.write(citra, "jpg", new File(path_preprocessing+"/"+"grayscaling "+fileEntry.getName()));

                        //citra hasil segmentasi otsu
                        citra = otsu.multilevelOTSU(citra, 5);
                        ImageIcon icon4 = new ImageIcon(citra);
                        trainSegmentasi.setIcon(icon4);
                        ImageIO.write(citra, "jpg", new File(path_preprocessing+"/"+"segmentasi "+fileEntry.getName()));

                        //citra penyakit
                        BufferedImage resc = ImageIO.read(new File(path_preprocessing+"/"+"cerah "+fileEntry.getName()));
                        resc = img.konvolusi(citra, resc);
                        ImageIcon icon5 = new ImageIcon(resc);
                        trainPenyakit.setIcon(icon5);
                        ImageIO.write(resc, "jpg", new File(path_otsu+"/"+"penyakit "+fileEntry.getName()));

                        //citra tak penyakit
                        BufferedImage resc2 = ImageIO.read(new File(path_preprocessing+"/"+"cerah "+fileEntry.getName()));
                        citra = img.colorSwitch(citra);
                        resc2 = img.konvolusi(citra, resc2);
                        ImageIcon icon6 = new ImageIcon(resc2);
                        trainTakPenyakit.setIcon(icon6);
                        ImageIO.write(resc2, "jpg", new File(path_otsu+"/"+"tak penyakit "+fileEntry.getName()));

                        String[] parts = fileEntry.getName().split("\\.");

                        jTextField1.setText(fileEntry.getName());
                        jTextField2.setText(String.valueOf(citra.getWidth()));
                        jTextField3.setText(String.valueOf(citra.getHeight()));

                        img.getFeature(resc);

                        trainr.setText(String.valueOf(img.nilair));
                        traing.setText(String.valueOf(img.nilaig));
                        trainb.setText(String.valueOf(img.nilaib));
                        
                        String nama = fileEntry.getName();
                        int panjang = nama.length()-4;
                        nama = nama.substring(0, panjang);
                        dataSetModel.addRow(new Object[]{nama,trainr.getText(), traing.getText(), trainb.getText(), parts[0]});

                    } catch(IOException e){
                        e.getMessage();
                    }
                }
            }
        };
        t.start();
    }//GEN-LAST:event_btnTrainingActionPerformed

